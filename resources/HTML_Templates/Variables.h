// StyleSheetFileURLString = URL where stylesheet lives

// ----------------------------------------------------------
// ItemList specific
// ----------------------------------------------------------
// ItemValueValueString = value of itemvalue encoded as string, whatever this will be

// ----------------------------------------------------------
// Item specific
// ----------------------------------------------------------
// Identifier = Identifier like in MBItemType.h
// NumberOfValues = number of encoded valuedicts
// PrintSelf = print with Item itself? (0,1)
// Values = The array of Value Dicts
// ItemNameString = Item name

// ----------------------------------------------------------
// Std general
// ----------------------------------------------------------
// Identifier = Identifier like in MBItemType.h
// ItemValueNameString = The name of the ItemValue
// ItemNameString = Item name
// ItemValueTypeString = sText, Number, Currency, Date, Bool, URL
// ItemValueCommentString = Comment
// ItemValueCreationDateString = creation date as string
// ItemValueEncryptedString = "Yes/No"
// ----------------------------------------------------------
// Std ItemValue specific
// ----------------------------------------------------------
// ItemValueTypeValString = "Textvalue", "Datevalue", "Numbervalue", "Currencyvalue", "Boolvalue", "URLvalue"
// ItemValueData = value of the general itemvalue as string

// ----------------------------------------------------------
// Special general
// ----------------------------------------------------------
// ItemValueDataLocationString = "Internal" or "External"
// ItemValueLocationURLString = "empty" or "URL"
// ----------------------------------------------------------
// ExtendedText ItemValue specific
// ----------------------------------------------------------
// ETextValueTextTypeString = text type (TXT, RTF, RTFD)
// ETextValuePreviewData = preview of the text data, should be TXT, if we do not find a way to convert RTF to HTML
// ----------------------------------------------------------
// Image ItemValue specific
// ----------------------------------------------------------
// ImagePreviewURL = URL for IMG HTML tag where the preview image is found
// ImageValueTypeString = Image type string "jpeg", "png", ...
// ImageValueDimensionString = Image dimensions in pixel (width x height)
// ImageValueSizeString = Image size in kbytes
// ----------------------------------------------------------
// File ItemValue specific
// ----------------------------------------------------------
// FileValueTypeString = File type string "swx", "pdf", ...
// FileValueTypeIconURL = URL for IMG HTML tag where the filetype icon if found
// FileValueSizeString = Filesize in kbytes

